package com.academetrics.service;

import com.academetrics.dto.UserDTO;
import com.academetrics.entity.User;
import com.academetrics.repo.UserRepository;
import jakarta.transaction.Transactional;
import org.modelmapper.ModelMapper;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

@Service
@Transactional
public class UserService {

    @Autowired // This means to get the bean called userRepositor // Which is auto-generated by Spring, we will use it to handle the data
    private UserRepository userRepository;

    @Autowired
    private ModelMapper modelMapper;

    public UserDTO saveUser(UserDTO userDTO){   // change return type ; used this for testing
        userRepository.save(modelMapper.map(userDTO, User.class));
        return userDTO;
    }

    public Iterable<User> getAllUsers(){
        return userRepository.findAll();
    }


}
